function calc() {
  const get = (p, n) => Array.from({ length: n }, (_, i) =>
    +document.querySelector(`input[name="${p}${i + 1}"]:checked`)?.value || 3
  ).reduce((a, b) => a + b, 0);

  const e = get('E', 8), s = get('S', 8), t = get('T', 8), j = get('J', 8);

  // 将原始分转换为百分比（满分 40 分）
  const eP = Math.round(e / 40 * 100);
  const sP = Math.round(s / 40 * 100);
  const tP = Math.round(t / 40 * 100);
  const jP = Math.round(j / 40 * 100);

  // 三分法：根据百分比判断等级
  // < 30% -> 1 (低), 30%-70% -> 2 (中), > 70% -> 3 (高)
  const getLevel = (p) => p < 30 ? 1 : (p <= 70 ? 2 : 3);

  const eL = getLevel(eP);
  const sL = getLevel(sP);
  const tL = getLevel(tP);
  const jL = getLevel(jP);

  // 生成类型编码，如 E1S2T3J1
  const type = `E${eL}S${sL}T${tL}J${jL}`;

  return { eP, sP, tP, jP, eL, sL, tL, jL, type };
}

